    public BusLayoutGUI(String username, int busId, Date date) {
        super("Select Seat");
        initComponents();
        //setSize(1350, 730);
        this.setExtendedState(this.getExtendedState() | JFrame.MAXIMIZED_BOTH);
        this.username = username;
        this.busId = busId;
        this.date = date;
        source = bus.getSource(busId);
        srctxt.setText(source);
        destination = bus.getDestination(busId);
        desttxt.setText(destination);
        arrivaltime = bus.getArrivalTime(busId);
        arvltimetxt.setText(arrivaltime);
        departuretime = bus.getDepartureTime(busId);
        depttimetxt.setText(departuretime);
        amount = bus.getAmount(busId);
        amttxt.setText(Double.toString(amount));
        name = home.getName(username);
        nametxt.setText(name);
        datetxt.setDate(date);
        bseats = ticket.bookedSeats(busId, date);
               
        seat = new JButton[21];
        for(int i = 0; i < 21; i++){
            seat[i] = new JButton(String.valueOf(i+1));
            seat[i].setSize(5, 5);
            jPanel3.add(seat[i]);
            for(int j =0; j<bseats.size();j++){
                if(bseats.get(j)==i+1)
                    seat[i].setEnabled(false);
            }
            
            addActionListenerTo(seat[i],i);      
        }
        
       
    }
