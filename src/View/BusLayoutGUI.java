    private void confirmButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmButtonActionPerformed
        // TODO add your handling code here:
        if(seattxt.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Select a seat");
        }
        else{
            name = nametxt.getText();
            seatno = Integer.parseInt(seattxt.getText());
            amount = Double.parseDouble(amttxt.getText());
            ticketno = ticket.newTicket(username, name, busId, amount, seatno,date);
            PrintTicketGUI printTicketGUI = new PrintTicketGUI(ticketno);
            this.setVisible(false);
            printTicketGUI.setVisible(true);
            
        }
        
    }//GEN-LAST:event_confirmButtonActionPerformed

    
